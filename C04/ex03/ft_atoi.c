#include <stdio.h>

int	ft_atoi(char *str)
{
	int	n;
	int	i;
	int	sgn;

	i = 0;
	sgn = 1;
	n = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sgn *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	n = sgn * n;
	return (n);
}
