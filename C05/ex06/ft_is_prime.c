int	ft_is_prime(int nb)
{
	int	div;
	int	i;

	div = 0;
	if (nb < 2)
		return (0);
	else
	{
		i = 2;
		while (i < nb)
		{
			if (nb % i == 0)
				return (0);
			i++;
			if (i > 3)
				i++;
		}
	}
	return (1);
}
