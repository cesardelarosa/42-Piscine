int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] == s2[i])
	{
		if (i + 1 == n || s1[i] == '\0')
			return (0);
		i++;
	}
	return (s1[i] - s2[i]);
}
